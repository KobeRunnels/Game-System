void clearSmallSprite(int array[20][20],int xx, int yy, int value)
{
	int x,y;
	for(y=0;y<20;y++)
	{
		for(x=0;x<20;x++)
		{
			if(array[y][x] != 0)
			{
				switch(value)
				{
					case 2:
					if(x%3 == 0)
						setColor(B);
					else if(x%2 == 0)
						setColor(4);
					else
						setColor(F);	
					break;
					case 3:
					if(x%10 == y%10)
						setColor(A);
					else if(x%2==0)
						setColor(1);	
					break;
					case 0:	
					setColor(G);
					break;
					case 1:
					if(x%2 == 0)
						setColor(1);
					else
						setColor(2);	
					break;
				}
				printAt(219,x+xx,y+yy);	
			}
		}
	}
	setColor(F);
}

