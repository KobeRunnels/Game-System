void printSmallSprite(int array[20][20],int xx, int yy) // for printing original stone crawler sprites
{
	int x,y;
	for(y=0;y<20;y++)
	{
		for(x=0;x<20;x++)
		{
			if(array[y][x] != 0)
			{
				setColor(array[y][x]);
				printAt(219,x+xx,y+yy);	
			}
		}
	}
	setColor(F);
}

